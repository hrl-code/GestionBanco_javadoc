    private void jButtonAsociarEntidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAsociarEntidadActionPerformed
       
       
       if ("".equals(jTextFieldCodigoEnt.getText()) || "".equals(jTextFieldMaximoAut.getText()) ) {
            jTextFieldMaximoAut.setBackground(Color.red);
            jTextFieldCodigoEnt.setBackground(Color.red);
            JOptionPane.showMessageDialog(null, "Las entidades autorizadas deben tener un cï¿½digo \n"
                   + "y deben tener una cantidad mï¿½xima permitida.");
            jTextFieldMaximoAut.setBackground(Color.white);
            jTextFieldCodigoEnt.setBackground(Color.white);
           
       } else {
           cod = jTextFieldCodigoEnt.getText();
           maxAutori = Double.parseDouble(jTextFieldMaximoAut.getText());
           
            if (!comprobarPositivo (maxAutori)) {
           
                jTextFieldMaximoAut.setForeground(Color.red);
                JOptionPane.showMessageDialog(null, "El mï¿½ximo autorizado debe ser positivo.\nTeclee de nuevo la cantidad");
                jTextFieldMaximoAut.setForeground(Color.black);
                jTextFieldMaximoAut.setText("");
             
            } else if (entidadesAutorizadas.containsKey(cod)){
                int op = JOptionPane.showConfirmDialog(null, "Ya existe una entidad almacenada con ese cï¿½digo.\n"
                         + "ï¿½Desea asociar esa entidad a la cuenta bancaria actual con el mï¿½ximo permitido asociado a la misma?");
                
                if (op ==0) {
                    micuentaPersonal.almacenarEntidadesPropias(cod, entidadesAutorizadas.get(cod));
                    jTextAreaEntidades.setText(misentidades.toString());
                    jTextFieldCodigoEnt.setText("");
                    jTextFieldMaximoAut.setText("");
                }
           
            } else {
                micuentaPersonal.almacenarEntidadesPropias(cod, maxAutori);
                entidadesAutorizadas.put(cod, maxAutori);
              
                jTextAreaEntidades.setText(misentidades.toString());
                jTextFieldCodigoEnt.setText("");
                jTextFieldMaximoAut.setText("");
            }
       }
      
    }//GEN-LAST:event_jButtonAsociarEntidadActionPerformed

