    private void jButtonComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComprobarActionPerformed
       
        numCuenta =jTextFieldNumeroCuenta.getText().trim().replace("-", "").replace(" ", "");

        if (numCuenta.length()!=20) {
                
            JOptionPane.showMessageDialog(null, "Error en el nï¿½mero de cuenta: debe tener viente dï¿½gitos\n"
                        + "Revise el nï¿½mero de cuenta.");
            jTextFieldNumeroCuenta.setForeground(Color.red);
        }

        else if (!micuentaPersonal.comprobarDigitosControl(numCuenta)) {
            
            JOptionPane.showMessageDialog(null, "Error en el nï¿½mero de cuenta: los dï¿½gitos de control no se corresponden con el nï¿½mero tecleado\n"
                        + "Revise el nï¿½mero de cuenta.");
            jTextFieldNumeroCuenta.setForeground(Color.red);
            
        // Comprobamos que el nï¿½mero de cuenta no estï¿½ ya en uso
        }else if (miscuentas.containsKey(numCuenta)){
            
             JOptionPane.showMessageDialog(null, "El nï¿½mero estï¿½ asociado a una cuenta existente\n"
                        + "Teclee un nuevo nï¿½mero de cuenta");
             jTextFieldNumeroCuenta.setForeground(Color.red);
             
        } else {
            micuentaPersonal.setNumeroCuenta(numCuenta);
            
            //En caso de estar todo correcto en el nï¿½mero de cuenta activamos el resto de campos del formulario
            //desactivando el campo numero de cuenta y el botï¿½n comprobar
            jTextFieldNumeroCuenta.setForeground(Color.BLACK);
            JOptionPane.showMessageDialog(null, "Nï¿½mero correcto y diponible para el nuevo cliente");
            jTextFieldSaldoInicial.setEditable(true);
            jTextFieldMantenimiento.setEditable(true);
            jTextFieldCodigoEnt.setEditable(true);
            jTextFieldMaximoAut.setEditable(true);
            jButtonAsociarEntidad.setEnabled(true);
            jTextFieldNombre.setEditable(true);
            jTextFieldApellidos.setEditable(true);
            jDateChooserFechaNacimiento.setEnabled(true);
            jButtonRegistrar.setEnabled(true);
            jTextFieldNumeroCuenta.setEditable(false);
            jButtonComprobar.setEnabled(false);
        }
   
    }//GEN-LAST:event_jButtonComprobarActionPerformed

    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        
        if (!comprobarCampos()) {
            JOptionPane.showMessageDialog(null, "Revise los campos del formulario.\n"
                    + "Complete los campos vacï¿½os");
        } else {
            nuevoCliente.setNombre(jTextFieldNombre.getText());
            nuevoCliente.setApellidos(jTextFieldApellidos.getText());
            nuevoCliente.setFechaNacimiento(jDateChooserFechaNacimiento.getDate());
            saldoIn=Float.parseFloat(jTextFieldSaldoInicial.getText());
            comMantenimiento=Float.parseFloat(jTextFieldMantenimiento.getText());
           
            if (!comprobarPositivo(saldoIn)) {
                jTextFieldSaldoInicial.setForeground(Color.red);
                JOptionPane.showMessageDialog(null, "El saldo debe ser positivo.\nTeclee de nuevo el saldo");
                jTextFieldSaldoInicial.setForeground(Color.black);
                jTextFieldSaldoInicial.setText("");
            }else if (!comprobarPositivo (comMantenimiento)) {
                jTextFieldMantenimiento.setForeground(Color.red);
                JOptionPane.showMessageDialog(null, "La comisiï¿½n de mantenimiento no puede ser 0 negativa.\nTeclee de nuevo la cantidad");
                jTextFieldMantenimiento.setForeground(Color.black);
                jTextFieldMantenimiento.setText("");
          
            }else {

                micuentaPersonal.setSaldo(saldoIn);
                micuentaPersonal.setComisionMantenimiento(comMantenimiento);

                miscuentas.put(numCuenta, micuentaPersonal);
                JOptionPane.showMessageDialog(null, "Cuenta registrada correctamente");

                jTextFieldNumeroCuenta.setText("");
                jTextFieldNumeroCuenta.setEditable(true);
                jButtonComprobar.setEnabled(true);

                jTextFieldNombre.setText("");
                jTextFieldNombre.setEditable(false);
                jTextFieldApellidos.setText("");
                jTextFieldApellidos.setEditable(false);
                jDateChooserFechaNacimiento.setDate(null);
                jDateChooserFechaNacimiento.setEnabled(false);

                jTextFieldSaldoInicial.setText("");
                jTextFieldSaldoInicial.setEditable(false);
                jTextFieldMantenimiento.setText("");
                jTextFieldMantenimiento.setEditable(false);
                
                jTextFieldCodigoEnt.setEditable(false);
                jTextFieldMaximoAut.setEditable(false);
                jButtonAsociarEntidad.setEnabled(false);
                jTextAreaEntidades.setText("");
                
                jButtonRegistrar.setEnabled(false);
            }            
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

